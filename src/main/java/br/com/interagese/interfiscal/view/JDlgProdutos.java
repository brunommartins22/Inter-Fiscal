    public void definirFormulario(String texto) {
        a.iconApplication(this);
        this.setTitle("Pesquisa de Produtos");
        this.setLocationRelativeTo(null);
        JbtnSearch.setUI(new MetalButtonUI());
        jTextProd.setText(texto);
        produtosTableModel = new ProdutosTableModel((texto != null && !texto.equals("")) ? tabproBusiness.getProdutobyDescorCod(texto) : null);
        jTableProduto.setModel(produtosTableModel);
        a.setColumn(jTableProduto, 0, 100);
        a.setColumn(jTableProduto, 1, 150);
        a.setColumnMinWidth(jTableProduto, 2, 250);
    }

