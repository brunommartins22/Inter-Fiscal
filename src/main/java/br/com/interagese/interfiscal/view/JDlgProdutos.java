    private void JbtnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_JbtnSearchActionPerformed
        try {
            if (jTextProd.getText() != null && !jTextProd.getText().equals("")) {
                List<Tabpro> result = tabproBusiness.getProdutobyDescorCod(jTextProd.getText());
                if (!result.isEmpty()) {
                    produtosTableModel.setItems(result);
                } else {
                    OptionPane.showMessageDialog("Error", "Produto nÃ£o encontrado na Base de Dados !!", null, () -> {
                        Thread.sleep(1000);
                        return null; //To change body of generated lambdas, choose Tools | Templates.
                    });
                }

            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex.getLocalizedMessage());
        }
    }//GEN-LAST:event_JbtnSearchActionPerformed

    private void jTableProdutoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableProdutoMouseClicked
        if (evt.getClickCount() == 2) {
            Sessao.produto = produtosTableModel.getItem(jTableProduto.getSelectedRow());
            dispose();
        }
    }//GEN-LAST:event_jTableProdutoMouseClicked

